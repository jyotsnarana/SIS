
public class transcriptlist {
	
private String term, course, description, grade, GPA;

	public transcriptlist(String term, String course, String description, String grade, String GPA)
	{
		this.term=term;
		this.course=course;
		this.description=description;
		this.grade=grade;
		this.GPA=GPA;
	}
	public String getterm()
	{
		return term;
	}
	public String getcourse()
	{
		return course;
	}
	public String getdescription()
	{
		return description;
	}
	public String getgrade()
	{
		return grade;
	}
	public String getGPA()
	{
		return GPA;
	}






}
